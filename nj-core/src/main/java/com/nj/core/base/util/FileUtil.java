	/**
	 * Mapped File way MappedByteBuffer 可以在处理大文件时，提升性能
	 * 
	 * @param filename
	 * @return
	 * @throws IOException
	 */
	public static byte[] toByteArray3(String filePath) throws IOException {

		FileChannel fc = null;
		RandomAccessFile rf = null;
		try {
			rf = new RandomAccessFile(filePath, "r");
			fc = rf.getChannel();
			MappedByteBuffer byteBuffer = fc.map(MapMode.READ_ONLY, 0, fc.size()).load();
			// System.out.println(byteBuffer.isLoaded());
			byte[] result = new byte[(int) fc.size()];
			if (byteBuffer.remaining() > 0) {
				// System.out.println("remain");
				byteBuffer.get(result, 0, byteBuffer.remaining());
			}
			return result;
		} catch (IOException e) {
			e.printStackTrace();
			throw e;
		} finally {
			try {
				rf.close();
				fc.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
	}

	/**
	 * 复制文件
	 * @param source	源文件的路径，指向一个文件
	 * @param target	目标文件的路径，指向一个文件
	 * @param overlay	是否覆盖，true是  false不覆盖
	 */
	public static Map<String,String> copyFile(String source,String target,boolean overlay){
		
		Map<String,String> result = new HashMap<String,String>();
		result.put("code", "sucess");
		
		File sourcefile = new File(source);
		if(!sourcefile.exists()){
			result.put("code", "fail");
			result.put("msg", "source文件不存在");
			return result;
		}
		
		if(sourcefile.isDirectory()){
			result.put("code", "fail");
			result.put("msg", "source为目录，不是一个文件");
			return result;
		}
		
		File targetfile = new File(target);
		if(targetfile.exists()){
			if(overlay){
				targetfile.delete();
			}
		}else{
			if(!targetfile.getParentFile().exists()){
				targetfile.getParentFile().mkdirs();
			}
		}
		
		InputStream in = null;
		OutputStream out = null;
		try {
			in = new FileInputStream(sourcefile);
			out = new FileOutputStream(targetfile);
		
			int num = 0;
			byte[] buffer = new byte[1024];
			while((num = in.read(buffer)) != -1){
				out.write(buffer, 0, num);
			}
		} catch (IOException e) {
			e.printStackTrace();
		}finally {
			try {
				if(null != in){
					in.close();
				} 
				if(null != out){
					out.close();
				} 
			}catch (IOException e) {
				e.printStackTrace();
			}
		}
		
		return result;
	}
